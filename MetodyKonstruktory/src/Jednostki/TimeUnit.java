package Jednostki;

public class TimeUnit {
    double godzNaMinuty(double a){
        double result=a*60;
        return result;
    }
    double minutyNaSek(double a){
        double result=a*60;
        return result;
    }
    float sekNaMilisek(double a){
        float result= (float) (a*1000);
        return result;
    }
}

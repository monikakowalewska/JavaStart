public class ShorthandOperators {
    public static void main(String[] args) {
        int x = 10;
        x += 5;
        System.out.println(x); // x ma wartoœæ 15
        x *= 3;
        System.out.println(x); // x ma wartoœæ 45
        x /= 5;
        System.out.println(x); // x ma wartoœæ 9
        x -= 2;
        System.out.println(x); // x ma wartoœæ 7
        x %= 4;
        System.out.println(x);// x ma wartoœæ 3
    }

}
